/*******************************************************************************
 NotePixel: 弹出窗像素设置     Color: 像素颜色(1/0)  像素定位: Nx, Ny
*******************************************************************************/
void NotePixel(u8 Color)
{
  u16 i, j, *pBuf = (u16*)&WaveBuf[300 + 300 + 300];

  Nx = Nx%ParamTab[WXx1];
  Ny = Ny%ParamTab[HYx2];             // Nx, Ny 超界则返回起点
  i  = Nx*(ParamTab[HYx2]/32)+ Ny/16;
  j  = Ny%16;
  if(Color) pBuf[i] |=  1 << j;
  else      pBuf[i] &=~(1 << j);
  Ny++;
}
